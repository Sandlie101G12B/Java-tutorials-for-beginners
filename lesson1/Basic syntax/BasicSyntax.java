
public class BasicSyntax {

    // The main method is the entry point of any Java application
    public static void main(String[] args) {
        // Print a message to the console
        System.out.println("Hello, World!");

        // Variable declaration and initialization
        int number = 10; // Integer variable to store whole numbers
        double decimal = 5.5; // Double variable to store decimal numbers
        String text = "Java"; // String variable to store text
        boolean flag = true; // Boolean variable to store true/false values

        // Printing variables to the console
        System.out.println("Number: " + number); // Prints the value of the integer variable
        System.out.println("Decimal: " + decimal); // Prints the value of the double variable
        System.out.println("Text: " + text); // Prints the value of the string variable
        System.out.println("Flag: " + flag); // Prints the value of the boolean variable
    }
}
